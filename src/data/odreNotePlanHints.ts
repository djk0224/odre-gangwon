export function getOdreNotePlanHint(
  noteId: string,
  fallbackLead?: string,
): OdreNotePlanHint | undefined {
  const hint = ODRE_NOTE_PLAN_HINTS[noteId];
  if (hint) return hint;
  if (fallbackLead?.trim()) {
    return { lines: [fallbackLead.trim(), ""] };
  }
  return undefined;
}
